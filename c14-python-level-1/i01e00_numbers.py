# variable declaration
n = 5
m = 10

# addition
the_sum = n + m
print(the_sum)

# subtraction
sub = m - n
print(sub)

# multiplication
product = n * m
print(product)

# division
quotient = 10 / 3
print(quotient)  # 3.3333333333333335

# integer division
int_quotient = 10 // 3
print(int_quotient)  # 3

# power
power = 4 ** 2
print(power)  # 16

# square root
root = 25 ** 0.5
print(root)  # 5

# square root
root = 25 ** (1 / 2)
print(root)  # 5
